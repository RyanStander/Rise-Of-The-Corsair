namespace Crew.Enums
{
    public enum CrewMemberHealth
    {
        Excellent,//Crew members in excellent health are in their prime physical condition. Gain bonuses to stats
        Good,//Crew members in good health are generally healthy and able to carry out their tasks without significant issues. No bonuses or penalties to stats
        Fair,//Crew members in fair health may have minor ailments or injuries that do not severely impact their ability to work. Minor penalties to stats
        Poor,//Crew members in poor health are suffering from moderate injuries or illnesses. Moderate penalties to stats
        Critical//Crew members in critical health are in a life-threatening condition. Severe penalties to stats
    }
}
